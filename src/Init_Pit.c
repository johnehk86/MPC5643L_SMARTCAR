/*********************************************************/


/*********************************************************/
/*			    	Function	         				*/
/*********************************************************/
void Init_Pit(void)
{
	Init_Pit0();
}

static void Init_Pit0(void)
{
	PIT.PITMCR.B.MDIS = 1U; /*Clock for Timers is disable*/
    PIT.CH[0].LDVAL.R = 20000U;
    PIT.CH[0].TCTRL.B.TIE = 1U;
    PIT.CH[0].TCTRL.B.TEN = 1U;
    PIT.CH[0].TFLG.R = 1U;
    INTC.PSR[59].R = 0x01u;
    PIT.PITMCR.B.MDIS = 0U; /*Clock for Timers is Enable*/
}
