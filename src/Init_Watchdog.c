/*********************************************************/
/*			    Static Function Prototype		         */
/*********************************************************/
static void DisableWatchdog(void);

/*********************************************************/
/*			    	Variable	         				*/
/*********************************************************/


/*********************************************************/
/*			    	Function	         				*/
/*********************************************************/
void Init_WatchDog(void)
{
	DisableWatchdog();
}


static void DisableWatchdog(void) 
{
    SWT.SR.R = 0x0000c520;     /* Write keys to clear soft lock bit */
    SWT.SR.R = 0x0000d928; 
    SWT.CR.R = 0xFF00010A;     /* Clear watchdog enable (WEN) */
}
